    private void readBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_readBActionPerformed
        if (flag){
        BufferedReader br = null;
        try{
            br = new BufferedReader(new FileReader("C:\\Users\\Gabriel\\Documents\\NetBeansProjects\\Cervejaria\\src\\main\\java\\com\\mycompany\\cervejaria\\Receitas.txt"));
            int val = Integer.parseInt(br.readLine());
            for(int i=0; i<val; i++){
            
                String ss = br.readLine();
                listModel.addElement(ss);
            }
            jList1.setModel(listModel);
            
            flag = false;
        }catch(Exception e){
        
            System.out.println(""+e);
        }finally{
            try {
            br.close();
            }catch(Exception e){
                System.out.println(""+e);
            }
        }
        }
    }//GEN-LAST:event_readBActionPerformed

