    private void addBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBActionPerformed
        String str = jTextField1.getText();
        if(str.equals("")||str.equalsIgnoreCase("Digite aqui"))
        {
            jTextField1.setText("Digite Aqui"); //mostra msg se campo de texto esta vazio
        }else {
            listModel.addElement(str); //add string to listmodel
            jList1.setModel(listModel); //set listmode to jlist
            jTextField1.setText(str); //clear text field
        
        }    
    }//GEN-LAST:event_addBActionPerformed

    private void writeBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_writeBActionPerformed
        // escrevendo num text file
        int val1 = jList1.getModel().getSize(); //pega o tam da jlist e guarda numa variavel
        PrintWriter writer = null;
        try{
            writer = new PrintWriter("C:\\Users\\Gabriel\\Documents\\NetBeansProjects\\Cervejaria\\src\\main\\java\\com\\mycompany\\cervejaria\\Receitas.txt");
            writer.println(val1); //escreve tamanho da jlist para file para nos ajudar a guardar numeros
            for(int i=0; i<val1;i++){
                //String s = jList1.getModel().getElementAt(i);
                writer.println(jList1.getModel().getElementAt(i)); //pega o elemento da jlist pelo index
            
            }
            
            
        }catch(Exception e){
        
            System.out.println(""+e);
        }finally{
    
            writer.close();
            }
    }//GEN-LAST:event_writeBActionPerformed

